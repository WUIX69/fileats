    <div class="faq-item">
        <h6 class="text-primary mb-3">01. What types of recipes can I submit?</h6>
        <p class="text-muted">
            You can submit any type of recipe including main dishes, desserts, appetizers,
            beverages, and snacks. Make sure your recipe is original or properly credited if
            adapted from another source.
        </p>
    </div>

    <!-- FAQ Item 2 -->
    <div class="faq-item">
        <h6 class="text-primary mb-3">02. How do I format my recipe instructions?</h6>
        <p class="text-muted">
            Break down your instructions into clear, numbered steps. Be specific with
            measurements, temperatures, and cooking times. Adding tips and notes can be
            helpful for other cooks.
        </p>
    </div>

    <!-- FAQ Item 3 -->
    <div class="faq-item">
        <h6 class="text-primary mb-3">03. What makes a good recipe photo?</h6>
        <p class="text-muted">
            Upload clear, well-lit photos of your finished dish. The photo should accurately
            represent the recipe and be visually appealing. Natural lighting works best for
            food photography.
        </p>
    </div>

    <!-- FAQ Item 4 -->
    <div class="faq-item">
        <h6 class="text-primary mb-3">04. Can I edit my recipe after submitting?</h6>
        <p class="text-muted">
            Yes, you can edit your recipe after submission. You can update ingredients,
            instructions, photos, and other details through your recipe management
            dashboard.
        </p>
    </div>

    <!-- FAQ Item 5 -->
    <div class="faq-item">
        <h6 class="text-primary mb-3">05. How long until my recipe is published?</h6>
        <p class="text-muted">
            Recipes typically undergo a brief review process to ensure quality and
            completeness. Most recipes are published within 24-48 hours after submission.
        </p>
